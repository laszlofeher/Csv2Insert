    private void openJMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openJMenuItemActionPerformed
        // TODO add your handling code here:
        if (fcs == null) {
            fcs = new JFileChooser();

        }
        fcs.addChoosableFileFilter(new GeneralFileFilter("csv"));
        fcs.showOpenDialog(this);

        try {
            CSVParser csv = new CSVParser(fcs.getSelectedFile());
            System.out.println(csv.getCharset());
            this.jTable1.setModel(new CSVTableModel(csv.parse()));
        } catch (IOException ex) {
            Logger.getLogger(Window.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_openJMenuItemActionPerformed

