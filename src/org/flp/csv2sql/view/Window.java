    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        // TODO add your handling code here:
        ((FieldsListModel) this.jList1.getModel()).addItem(this.fieldNameJTextField.getText());
        JTableHeader th = jTable1.getTableHeader();
        TableColumnModel tcm = th.getColumnModel();
        TableColumn tc = tcm.getColumn(0);
        tc.setHeaderValue(this.fieldNameJTextField.getText());
        th.repaint();
        ((CSVTableModel) this.jTable1.getModel()).addFieldName(this.fieldNameJTextField.getText());
        this.fieldNameJTextField.setText("");
    }//GEN-LAST:event_addJButtonActionPerformed

    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        // TODO add your handling code here:
        if (this.jList1.getSelectedIndex() > -1) {
            ((FieldsListModel) this.jList1.getModel()).removeItem(this.jList1.getSelectedIndex());
        }

    }//GEN-LAST:event_removeJButtonActionPerformed
